    private void jTDiscountKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTDiscountKeyReleased

        Double subTotal = Double.parseDouble(jLblSubTotal.getText());
        String discountStr = jTDiscount.getText();

        if (discountStr.isEmpty()) {
            discountStr = "0.00";
        }

        Double discount = Double.parseDouble(discountStr);
        Double total = subTotal * ((100 - discount) / 100);
        double totalroundOff = Math.round(total * 100.0) / 100.0;

        jLtotal.setText(String.valueOf(totalroundOff));
    }//GEN-LAST:event_jTDiscountKeyReleased

