    public boolean validateFields() {
        InputValidator validate = new InputValidator();
        if (!validate.validInvo(jLblInvoNo, "Invalied Invoice NO")) {
            return false;
        } else if (!validate.validateField(jTCusName, "Please enter Customer Name")) {
            return false;
        } else if (!validate.validateField(jTAddress, "Please enter Address")) {
            return false;
        }
        return true;
    }

    public void getVariables(String invoNo) {
        ItemDAO itemDAO = new ItemDAO();
        OutPrintDetails data = itemDAO.getStockOutToPrint(invoNo);
        jLblRepCode.setText(data.getRefCode());
    }

    public void getSubTotal(String invoNo) {
        ItemDAO itemDAO = new ItemDAO();
        String subTotal = itemDAO.getPrintSubTotal(invoNo);
        jLblSubTotal.setText(subTotal);
    }

